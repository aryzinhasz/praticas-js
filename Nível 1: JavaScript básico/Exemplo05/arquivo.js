//Manipulando Números com Prompt e Alert

let n = Number(prompt("Digite um número:"));
alert("O número digitado foi " + n);
alert("O dobro é " + n * 2);
alert("A metade é " + n / 2);

//Esse código solicita ao usuário um número, calcula o dobro e a metade desse número, e exibe os resultados usando alert.
